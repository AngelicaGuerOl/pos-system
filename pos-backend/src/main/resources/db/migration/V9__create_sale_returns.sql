ALTER TABLE receivables
    ADD CONSTRAINT chk_receivables_returned_amount_non_negative
        CHECK (returned_amount >= 0),
    ADD CONSTRAINT chk_receivables_returned_amount_not_greater_than_original
        CHECK (returned_amount <= original_amount),
    ADD CONSTRAINT chk_receivables_adjusted_amount_non_negative
        CHECK (adjusted_amount >= 0),
    ADD CONSTRAINT chk_receivables_adjusted_amount_math
        CHECK (adjusted_amount = original_amount - returned_amount),
    ADD CONSTRAINT chk_receivables_paid_amount_not_greater_than_adjusted
        CHECK (paid_amount <= adjusted_amount),
    ADD CONSTRAINT chk_receivables_balance_math
        CHECK (outstanding_balance = adjusted_amount - paid_amount),
    ADD CONSTRAINT chk_receivables_pending_fields
        CHECK (
            status <> 'PENDING'
            OR (
                paid_amount = 0
                AND outstanding_balance = adjusted_amount
                AND adjusted_amount > 0
                AND paid_at IS NULL
            )
        ),
    ADD CONSTRAINT chk_receivables_paid_fields
        CHECK (
            status <> 'PAID'
            OR (
                adjusted_amount > 0
                AND outstanding_balance = 0
                AND paid_at IS NOT NULL
            )
        ),
    ADD CONSTRAINT chk_receivables_cancelled_fields
        CHECK (
            status <> 'CANCELLED'
            OR (
                adjusted_amount = 0
                AND paid_amount = 0
                AND outstanding_balance = 0
                AND paid_at IS NULL
            )
        );

