            OR (type = 'SALE_REFUND' AND direction = 'OUTFLOW')
        );

CREATE TABLE sale_returns
(
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sale_id              BIGINT         NOT NULL,
    cash_session_id      BIGINT,
    processed_by_user_id BIGINT         NOT NULL,
    total_amount         NUMERIC(12, 2) NOT NULL,
    cash_refund_amount   NUMERIC(12, 2) NOT NULL DEFAULT 0,
    reason               VARCHAR(255)   NOT NULL,
    created_at           TIMESTAMPTZ    NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_sale_returns_sale
        FOREIGN KEY (sale_id)
            REFERENCES sales (id)
            ON UPDATE RESTRICT
            ON DELETE RESTRICT,

    CONSTRAINT fk_sale_returns_cash_session
        FOREIGN KEY (cash_session_id)
            REFERENCES cash_sessions (id)
            ON UPDATE RESTRICT
            ON DELETE RESTRICT,

    CONSTRAINT fk_sale_returns_processed_by_user
        FOREIGN KEY (processed_by_user_id)
            REFERENCES users (id)
            ON UPDATE RESTRICT
            ON DELETE RESTRICT,

    CONSTRAINT chk_sale_returns_total_amount_positive
        CHECK (total_amount > 0),

    CONSTRAINT chk_sale_returns_cash_refund_amount_non_negative
        CHECK (cash_refund_amount >= 0),

    CONSTRAINT chk_sale_returns_cash_refund_amount_not_greater_than_total
        CHECK (cash_refund_amount <= total_amount),

    CONSTRAINT chk_sale_returns_reason_not_blank
        CHECK (length(trim(reason)) > 0),

    CONSTRAINT chk_sale_returns_cash_session_required_for_refund
        CHECK (cash_refund_amount = 0 OR cash_session_id IS NOT NULL)
);

CREATE TABLE sale_return_items
(
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sale_return_id BIGINT         NOT NULL,
    sale_item_id   BIGINT         NOT NULL,
    product_id     BIGINT         NOT NULL,
    quantity       NUMERIC(10, 2) NOT NULL,
    unit_price     NUMERIC(12, 2) NOT NULL,
    subtotal       NUMERIC(12, 2) NOT NULL,

    CONSTRAINT fk_sale_return_items_sale_return
        FOREIGN KEY (sale_return_id)
            REFERENCES sale_returns (id)
            ON UPDATE RESTRICT
            ON DELETE RESTRICT,

    CONSTRAINT fk_sale_return_items_sale_item
        FOREIGN KEY (sale_item_id)
            REFERENCES sale_items (id)
            ON UPDATE RESTRICT
            ON DELETE RESTRICT,

    CONSTRAINT fk_sale_return_items_product
        FOREIGN KEY (product_id)
            REFERENCES products (id)
            ON UPDATE RESTRICT
            ON DELETE RESTRICT,

    CONSTRAINT chk_sale_return_items_quantity_positive
        CHECK (quantity > 0),

    CONSTRAINT chk_sale_return_items_unit_price_non_negative
        CHECK (unit_price >= 0),

    CONSTRAINT chk_sale_return_items_subtotal_non_negative
        CHECK (subtotal >= 0),

    CONSTRAINT uq_sale_return_items_return_sale_item
        UNIQUE (sale_return_id, sale_item_id)
);

